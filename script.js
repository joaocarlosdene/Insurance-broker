$(document).ready(function() {
    var botao = $('.dropdown-toggle');
    var dropDown = $('.dropdown-menu');    
   
       botao.on('click', function(event){
           dropDown.stop(true,true).slideToggle();
           event.stopPropagation();
       });
   });

